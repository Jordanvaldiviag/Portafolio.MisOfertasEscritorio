    //Carga de datos
    public void CargarDatos(){
        
        //JComboBox Regiones
        ArrayList <Regiones> regiones;
        regiones = serviceAdditional.ListarRegiones();
        
        for (Regiones regione : regiones) {
            cbcRegion.addItem(regione.getRegion());
        }
        //Fin carga JComboBox
        
        //Datos de empresas
        int idEmpresa = MaintainerBusiness.varSessionEmpresa;
        
        ServiceEmpresa servicio = new ServiceEmpresa();
        
        ArrayList<Empresas> listaEmpresas;
        listaEmpresas = servicio.ListarEmpresas();
        
        for (int i = 0; i < listaEmpresas.size(); i++) {
            if (listaEmpresas.get(i).getIDEmpresa() == idEmpresa) {
                txtRazonSocial.setText(listaEmpresas.get(i).getNombre());
                txtRut.setText(listaEmpresas.get(i).getRut());
                txtTelefono.setText(Long.toString(listaEmpresas.get(i).getTelefono()));
                txtActividad.setText(listaEmpresas.get(i).getActividad());
                cbcRegion.setSelectedItem(listaEmpresas.get(i).getRegion());
                cbcComuna.setSelectedItem(listaEmpresas.get(i).getComuna());
                txtDireccion.setText(listaEmpresas.get(i).getDireccion());
                break;
            }
        }
        //Fin empresa
        
    }
    
    //Comienzo de Carga de JComboBox dinamico y dependiente
    ServiceAdditional serviceAdditional = new ServiceAdditional();
    
    public void RellenarComuna(String selectItemCbc){
        cbcComuna.removeAllItems();
        ArrayList <Regiones> regiones;
        regiones = serviceAdditional.ListarRegiones();
        
        for (int i = 0; i < regiones.size(); i++) {
            if (regiones.get(i).getRegion().equals(selectItemCbc)) {
                cbcComuna.setModel(new javax.swing.DefaultComboBoxModel<>(regiones.get(i).getComunas()));
                break;
            }
        }
    }
