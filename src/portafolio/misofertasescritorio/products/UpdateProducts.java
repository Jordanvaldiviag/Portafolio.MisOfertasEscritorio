    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        String nombre = txtNombreProducto.getText();
        String precioNormal0 = txtPrecioProducto.getText();
        String descripcion = txtDescripcion.getText();
        String marca = txtMarca.getText();
        String modelo = txtModelo.getText();
        String stock0 = txtStockProducto.getText();
        String imagen = txtImagen.getText();
        String fechaVencimiento = txtFechaVencimiento.getText();
        String temporada = txtTemporada.getText();
        Categoria categoria = (Categoria)cbcCategoriaProducto.getSelectedItem();
        long idCategoria = categoria.getIDCategoria();
        Empresas empresa = (Empresas)cbcEmpresa.getSelectedItem();
        long idEmpresa = empresa.getIDEmpresa();
        
        //Formateo de entradas
        Long precioNormal1 = null;
        Long stock1 = null;
        
        //Fin de formateo de entradas
        
        //Validaciones
        ArrayList<String> listaErrores = new ArrayList<>();
        
        if (!Validations.validarNoVacio(nombre)) {
            listaErrores.add("El nombre del producto no puede estar en blanco");
        }
        
        if (!Validations.validarNoVacio(precioNormal0)) {
            listaErrores.add("El precio del producto no puede estar en blanco");
        }
        precioNormal1 = Validations.validarNumeroLong(precioNormal0);
        if (precioNormal1 == Long.parseLong("0")) {
            listaErrores.add("El precio del producto no puede contener letras");
        }
        
        if (!Validations.validarNoVacio(descripcion)) {
            listaErrores.add("La descripcion del producto no puede estar en blanco");
        }
        
        if (!Validations.validarNoVacio(marca)) {
            listaErrores.add("La marca del producto no puede estar en blanco");
        }
        
        if (!Validations.validarNoVacio(modelo)) {
            listaErrores.add("El modelo del producto no puede estar en blanco");
        }
        
        
        if (!Validations.validarNoVacio(stock0)) {
            listaErrores.add("El stock del producto no puede estar en blanco");
        }
        stock1 = Validations.validarNumeroLong(stock0);
        if (stock1 == Long.parseLong("0")) {
            listaErrores.add("El stock no puede contener letras");
        }
        
        if (!Validations.validarNoVacio(temporada)) {
            listaErrores.add("La temporada del producto no puede estar en blanco");
        }
        //Fin validaciones
        
        //Agregar producto
        if (listaErrores.isEmpty()) {
            ProductoHelper producto = new ProductoHelper(nombre, precioNormal1, descripcion, marca, modelo, stock1, imagen, fechaVencimiento, temporada, idEmpresa, idCategoria);
            servicio.ActualizarProducto(producto);
            dispose();
        }else{
            String errores = "";
            for (int i = 0; i < listaErrores.size(); i++) {
                errores += listaErrores.get(i)+"\n";
            }
            JOptionPane.showMessageDialog(null, errores);
        }
        //Fin de agregar producto
        
        
        
        
    }//GEN-LAST:event_btnActualizarActionPerformed

    
    public void CargarComponentes(){
        //Recuperar lista de categoria
        ArrayList<Categoria> listaCategoria;
        listaCategoria = servicio.ListarCategorias();
        
        for (int i = 0; i < listaCategoria.size(); i++) {
            cbcCategoriaProducto.addItem(listaCategoria.get(i));
        }
        
        //Recuperar lista de empresas
        ArrayList<Empresas> listaEmpresas;
        listaEmpresas = servicioEmpresa.ListarEmpresas();
        
        for (int i = 0; i < listaEmpresas.size(); i++) {
            cbcEmpresa.addItem(listaEmpresas.get(i));
        }
    }
    
    public void CargarProducto(){
        int idProducto = MaintainerProducts.varSessionProducto;
        ArrayList<ProductoElement> listaProductos;
        listaProductos = servicio.ListarProductos();
        
        for (int i = 0; i < listaProductos.size(); i++) {
            if (idProducto == listaProductos.get(i).getIDProducto()) {
                
                txtNombreProducto.setText(listaProductos.get(i).getNombre());
                txtPrecioProducto.setText(Long.toString(listaProductos.get(i).getPrecioNormal()));
                txtDescripcion.setText(listaProductos.get(i).getDescripcion());
                txtMarca.setText(listaProductos.get(i).getMarca());
                txtModelo.setText(listaProductos.get(i).getModelo());
                txtStockProducto.setText(Long.toString(listaProductos.get(i).getStock()));
                txtImagen.setText(listaProductos.get(i).getImagen());
                txtFechaVencimiento.setText(listaProductos.get(i).getFechaVencimiento());
                txtTemporada.setText(listaProductos.get(i).getTemporada());
                cbcCategoriaProducto.setSelectedItem(recuperarCategoria(listaProductos.get(i).getIDCategoria()));
                cbcEmpresa.setSelectedItem(recuperarEmpresa(listaProductos.get(i).getIDEmpresa()));
                
            }
        }
        
    }
    
    public Categoria recuperarCategoria(long id){
        ArrayList<Categoria> listaCategoria;
        listaCategoria = servicio.ListarCategorias();
        Categoria categoria = null;
        
        for (int i = 0; i < listaCategoria.size(); i++) {
            if (id == listaCategoria.get(i).getIDCategoria()) {
                categoria = listaCategoria.get(i);
            }
        }
        return categoria;
    }
    
    public Empresas recuperarEmpresa(long id){
        ArrayList<Empresas> listaEmpresas;
        listaEmpresas = servicioEmpresa.ListarEmpresas();
        Empresas empresa = null;
        
        for (int i = 0; i < listaEmpresas.size(); i++) {
            if (id == listaEmpresas.get(i).getIDEmpresa()) {
                empresa = listaEmpresas.get(i);
            }
        }
        return empresa;
    }
    
